package OOP;

public class Author {
    public static String surname;
    public static String name;

    public void setName(String name) {
        Author.name = name;
    }

    public void setSurname(String surname) {
        Author.surname = surname;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

}


